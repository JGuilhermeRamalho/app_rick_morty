class Character {
  final int id;
  final String name;
  final String status;
  final String species;
  final String image;
  final String type;
  final String gender;

  Character({
    required this.id,
    required this.name,
    required this.status,
    required this.species,
    required this.image,
    required this.type,
    required this.gender
  });
}
